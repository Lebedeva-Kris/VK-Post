package domain;

public class ViewsInfo {
    private int viewId;
    private int viewCount;


    public int getViewId() {
        return viewId;
    }
    public void setViewId(int viewId) {
        this.viewId = viewId;
    }


    public int getViewCount() {
        return viewCount;
    }
    public void setViewCount(int viewCount) {
        this.viewCount = viewCount;
    }

}
