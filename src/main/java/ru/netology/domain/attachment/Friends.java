package ru.netology.domain.attachment;

public class Friends {
    private int firstFriendId;
    private int secondFriendId;
    private int thirdFriendId;


    public int getFirstFriendId() {
        return firstFriendId;
    }
    public void setFirstFriendId(int firstFriendId) {
        this.firstFriendId = firstFriendId;
    }


    public int getSecondFriendId() {
        return secondFriendId;
    }
    public void setSecondFriendId(int secondFriendId) {
        this.secondFriendId = secondFriendId;
    }


    public int getThirdFriendId() {
        return thirdFriendId;
    }
    public void setThirdFriendId(int thirdFriendId) {
        this.thirdFriendId = thirdFriendId;
    }

}
